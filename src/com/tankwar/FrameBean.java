package com.tankwar;

public class FrameBean {
private int gwidth;
private int gheight;
/**
 * @return the gwidth
 */
public int getGwidth() {
	return gwidth;
}
/**
 * @return the gheight
 */
public int getGheight() {
	return gheight;
}
/**
 * @param gwidth the gwidth to set
 */
public void setGwidth(int gwidth) {
	this.gwidth = gwidth;
}
/**
 * @param gheight the gheight to set
 */
public void setGheight(int gheight) {
	this.gheight = gheight;
}
}
